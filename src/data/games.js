// ── Helpers ──────────────────────────────────────────────────

// Extract YouTube video ID from a full URL
export function getYouTubeId(url) {
  if (!url) return null
  const match = url.match(/(?:v=|youtu\.be\/)([A-Za-z0-9_-]{11})/)
  return match ? match[1] : null
}

// Next upcoming/live game on or after today
export function getNextGame() {
  const today = new Date()
  today.setHours(0, 0, 0, 0)
  return (
    games.find((g) => {
      const d = new Date(g.date + 'T00:00:00')
      return g.status !== 'completed' && g.status !== 'postponed' && d >= today
    }) ?? null
  )
}

// Most recent completed games (newest first)
export function getRecentGames(count = 3) {
  return games
    .filter((g) => g.status === 'completed')
    .slice()
    .reverse()
    .slice(0, count)
}
